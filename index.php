// FAQ Accordion Enhancement
document.addEventListener('DOMContentLoaded', function() {
    const faqQuestions = document.querySelectorAll('.faq-question');
    
    faqQuestions.forEach(function(question) {
        // Handle Bootstrap collapse events
        const targetId = question.getAttribute('data-target');
        const target = document.querySelector(targetId);
        if (target) {
            target.addEventListener('show.bs.collapse', function() {
                const icon = question.querySelector('i');
                const faqItem = question.closest('.faq-item');
                const accordionParent = question.closest('.faq-accordion');
                
                // Remove active from items in the same column only
                if (accordionParent) {
                    accordionParent.querySelectorAll('.faq-item').forEach(function(item) {
                        item.classList.remove('active');
                    });
                }
                
                if (icon) icon.style.transform = 'rotate(180deg)';
                if (faqItem) faqItem.classList.add('active');
            });
            
            target.addEventListener('hide.bs.collapse', function() {
                const icon = question.querySelector('i');
                const faqItem = question.closest('.faq-item');
                if (icon) icon.style.transform = 'rotate(0deg)';
                if (faqItem) faqItem.classList.remove('active');
            });
        }
    });
});

// Ensure Loan Products Section uses static grid (no carousel/slider)
document.addEventListener('DOMContentLoaded', function() {
    const loanProductsGrid = document.querySelector('.loan-products-grid');
    if (loanProductsGrid) {
        // Remove any slick carousel classes if accidentally added
        loanProductsGrid.classList.remove('slick-slider', 'slick-initialized', 'owl-carousel');
        
        // Ensure grid display is maintained
        if (window.getComputedStyle(loanProductsGrid).display !== 'grid') {
            loanProductsGrid.style.display = 'grid';
        }
        
        // Destroy any slick carousel if initialized
        if (typeof jQuery !== 'undefined' && jQuery(loanProductsGrid).hasClass('slick-initialized')) {
            jQuery(loanProductsGrid).slick('unslick');
        }
    }
});
